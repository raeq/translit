def main():
    libraries: dict[str, callable] = {}

    try:
        from disarm import transliterate

        libraries["disarm"] = transliterate
    except ImportError:
        print("disarm not installed, skipping")

    try:
        from unidecode import unidecode

        libraries["Unidecode"] = unidecode
    except ImportError:
        print("Unidecode not installed, skipping")

    try:
        from text_unidecode import unidecode as text_unidecode

        libraries["text-unidecode"] = text_unidecode
    except ImportError:
        print("text-unidecode not installed, skipping")

    try:
        from anyascii import anyascii

        libraries["anyascii"] = anyascii
    except ImportError:
        print("anyascii not installed, skipping")

    if not libraries:
        print("No libraries to benchmark!")
        sys.exit(1)

    # Group texts by script family
    groups: dict[str, list[tuple[str, str]]] = {}
    for label, (group, text) in BENCH_TEXTS.items():
        groups.setdefault(group, []).append((label, text))

    lib_names = list(libraries.keys())
    header = f"  {'Text':20s} {'chars':>5s}"
    for name in lib_names:
        header += f"  {name:>14s}"
    if len(lib_names) >= 2:
        header += f"  {'ratio':>7s}"

    print("=" * len(header))
    print("Transliteration Benchmark — Natural Wikipedia Texts")
    print("=" * len(header))

    all_results: list[tuple[str, int, dict[str, float]]] = []

    for group_name, texts in groups.items():
        print(f"\n── {group_name} {'─' * (len(header) - len(group_name) - 4)}")
        print(header)
        print("  " + "─" * (len(header) - 2))

        for label, text in texts:
            results: dict[str, float] = {}
            for name, func in libraries.items():
                cps = bench_library(name, func, text)
                results[name] = cps

            row = f"  {label:20s} {len(text):>5d}"
            for name in lib_names:
                row += f"  {results[name]:>12,.0f}/s"
            if len(lib_names) >= 2 and "disarm" in results:
                second = lib_names[1]
                if results[second] > 0:
                    ratio = results["disarm"] / results[second]
                    row += f"  {ratio:>6.1f}x"
            print(row)
            all_results.append((label, len(text), results))

    # Summary
    print(f"\n{'=' * len(header)}")
    print("Summary (geometric mean chars/sec across all texts)")
    print("─" * len(header))

    import math

    for name in lib_names:
        speeds = [r[name] for _, _, r in all_results if name in r]
        if speeds:
            geo_mean = math.exp(sum(math.log(s) for s in speeds) / len(speeds))
            print(f"  {name:20s}: {geo_mean:>12,.0f} chars/sec")


if __name__ == "__main__":
    main()
