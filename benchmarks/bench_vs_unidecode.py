"""Head-to-head benchmark: disarm vs Unidecode / text-unidecode / anyascii.

Benchmarks transliteration throughput across all major script families using
natural-language Wikipedia text (~2–4 KB each, no synthetic repetition).

Run after installing comparison libraries:
    pip install Unidecode text-unidecode anyascii
"""

import sys
import time


def bench_library(name: str, func, text: str, iterations: int = 5_000) -> float:
    """Benchmark a transliteration function, return chars/sec."""
    # Warmup
    for _ in range(100):
        func(text)
    start = time.perf_counter()
    for _ in range(iterations):
        func(text)
    elapsed = time.perf_counter() - start
    chars_per_sec = len(text) * iterations / elapsed
    return chars_per_sec


